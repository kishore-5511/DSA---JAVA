package DSA.Operators;
public class AssignmentOperators {
    public static void main(String args[]) {
        int a = 10;
        // a = a + 10;
        a += 10;    // this kind of statements are comparitively faster than the normal statements.
        System.out.println(a);
        // a = a - 10;
        a -= 10;
        System.out.println(a);
        // a = a * 10;
        a *= 10;
        System.out.println(a);
        // a = a / 10;
        a /= 10;
        System.out.println(a);
        // a = a % 10;
        a %= 10;
        System.out.println(a);

    }
}
