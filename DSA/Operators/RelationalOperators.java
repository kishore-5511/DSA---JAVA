package DSA.Operators;
public class RelationalOperators {
    public static void main(String args[]) {
        int a = 18;
        int b = 12;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println(a>=b);
        System.out.println(a<=b);
    }
}
